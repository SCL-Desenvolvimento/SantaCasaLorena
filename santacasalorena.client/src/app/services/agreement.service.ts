import { Injectable } from '@angular/core';
import { HttpClient, HttpErrorResponse } from '@angular/common/http';
import { catchError, map, Observable, throwError } from 'rxjs';
import { environment } from '../../environments/environment';
import { Agreement } from '../models/agreement';

@Injectable({
  providedIn: 'root',
})
export class AgreementService {
  private apiUrl = `${environment.apiUrl}/agreements`;

  constructor(private http: HttpClient) { }

  getAll(): Observable<Agreement[]> {
    return this.http.get<Agreement[]>(this.apiUrl).pipe(
      map(response => response.map(item => ({
        ...item,
        imageUrl: `${environment.imageServerUrl}${item.imageUrl}`
      }))),
      catchError(this.handleError)
    );
  }

  getById(id: number): Observable<Agreement> {
    return this.http.get<Agreement>(`${this.apiUrl}/${id}`).pipe(
      map(response => ({
        ...response,
        imageUrl: `${environment.imageServerUrl}${response.imageUrl}`
      })),
      catchError(this.handleError)
    );
  }

  create(dto: Agreement): Observable<Agreement> {
    // Remove 'id' from DTO as it's typically generated by the backend
    const { id, createdAt, updatedAt, ...createDto } = dto;
    return this.http.post<Agreement>(this.apiUrl, createDto).pipe(
      catchError(this.handleError)
    );
  }

  update(id: number, dto: Agreement): Observable<Agreement> {
    // Remove 'id', 'createdAt', 'updatedAt' from DTO as they are not updated by the client
    const { createdAt, updatedAt, ...updateDto } = dto;
    return this.http.put<Agreement>(`${this.apiUrl}/${id}`, updateDto).pipe(
      catchError(this.handleError)
    );
  }

  delete(id: number): Observable<void> {
    return this.http.delete<void>(`${this.apiUrl}/${id}`).pipe(
      catchError(this.handleError)
    );
  }

  updateAgreementStatus(id: number, status: 'active' | 'inactive' | 'pending'): Observable<void> {
    return this.http.patch<void>(`${this.apiUrl}/${id}/status`, { status }).pipe(
      catchError(this.handleError)
    );
  }

  private handleError(error: HttpErrorResponse): Observable<never> {
    let errorMessage = 'Ocorreu um erro. Tente novamente mais tarde.';
    if (error.error instanceof ErrorEvent) {
      errorMessage = `Erro: ${error.error.message}`;
    } else {
      errorMessage = error.error?.message || error.statusText || errorMessage;
    }
    return throwError(() => new Error(errorMessage));
  }
}

